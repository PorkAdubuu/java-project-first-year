
public class Looping {
    
    public static void main(String[] args) {
     
     int number = 0; 
     
     //do while loop
     
     do { 
     System.out.println("number is: ");
     number++;
     } while (number < 5);
     
     //forloop
     
     int x = 0; 
     for (x=0; x< 10;  x++) {
       System.out.println("ss" + x);
       }
     
     // while 
     int w = 0; 
     while (w <=5) { 
       System.out.println("while");
       w++;
       }
     
    }
 }